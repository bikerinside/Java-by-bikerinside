public class Solido
{
    // Membri

    // altezza del solido
    private double a;
    // base del solido
    private double b;
    // profondità del solido
    private double p;

    // Metodi Setter
    
    // Metodo per settare l'altezza del solido
    public void SetA(double a) { this.a = a; }
    // Metodo per settare base del solido
    public void SetB(double b) { this.b = b; }
    // Metodo per settare la profondità del solido
    public void SetP(double p) { this.p = p; }

    // Metodi Getter
    public double GetA() { return a; }
    // Metodo per settare base del solido
    public double GetB() { return b; }
    // Metodo per settare la profondità del solido
    public double GetP() { return p; }
    
}
